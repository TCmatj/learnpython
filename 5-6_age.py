age = float(input("please input your age:"))

if age < 2:
    print('You were a baby')
elif age < 4:
    print("You're learning to walk")
elif age < 13:
    print('You are a child')
elif age < 20:
    print('You are a teenager')
elif age < 65:
    print('You are a adult')
else:
    print('You are a old man')
    
